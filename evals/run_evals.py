import os
import json
import asyncio
from dotenv import load_dotenv

# Ensure we are running from project root or add src to path
import sys
import time
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))

from src.langgraphagenticai.LLMS.geminillm import GeminiLLM
from src.langgraphagenticai.graph.graph_builder import GraphBuilder
from langchain_core.messages import HumanMessage
from langchain_google_genai import ChatGoogleGenerativeAI
from langchain_core.prompts import PromptTemplate
from src.langgraphagenticai.vectorstore.retriever import Retriever

load_dotenv(override=True)

# Evaluator LLM
eval_llm = ChatGoogleGenerativeAI(model="gemini-2.0-flash", temperature=0, api_key=os.environ.get("GOOGLE_API_KEY"))

eval_prompt = PromptTemplate(
    input_variables=["question", "expected", "actual"],
    template="""You are an expert evaluator for a RAG system.
Given the question, the expected gold-standard answer, and the actual answer generated by the system, score the actual answer on two metrics:
1. Faithfulness/Accuracy: Does the actual answer convey the same true information as the expected answer?
2. Answer Relevance: Does it directly answer the user's question?

Output exactly a JSON object (no markdown formatting, just raw JSON) with this format:
{{"accuracy_score": <int 0-10>, "relevance_score": <int 0-10>, "reasoning": "<short reasoning>"}}

Question: {question}
Expected Answer: {expected}
Actual Answer: {actual}
"""
)

def run_evaluation():
    print("Setting up vector store...")
    retriever_manager = Retriever()
    # It reads from data.json directly
    asyncio.run(retriever_manager.set_retriever())
    
    print("Setting up Graph...")
    user_controls_input = {"selected_model": "gemini-2.0-flash"}
    obj_llm_config = GeminiLLM(user_controls_input=user_controls_input)
    model = obj_llm_config.get_llm_model()
    
    graph_builder = GraphBuilder(model=model, user_controls_input=user_controls_input)
    graph = graph_builder.setup_graph()
    
    print("Loading dataset...")
    dataset_path = os.path.join(os.path.dirname(__file__), "qa_dataset.json")
    with open(dataset_path, "r") as f:
        dataset = json.load(f)

    results = []
    total_acc = 0
    total_rel = 0

    print("Running evaluation...")
    for idx, item in enumerate(dataset):
        if idx > 0:
            print("Sleeping for 15s to avoid Google AI Studio rate limits...")
            time.sleep(15)
        
        q = item["question"]
        expected = item["expected_answer"]
        print(f"\nEvaluating Q{idx+1}: {q}")
        
        inputs = {"question": q, "generated": 0, "question_rewritten": False}
        # Run graph
        final_state = None
        for output in graph.stream(inputs):
            for key, value in output.items():
                final_state = value
                
        actual = final_state.get("generation", "No answer generated.")
        
        # Evaluate
        prompt_val = eval_prompt.format(question=q, expected=expected, actual=actual)
        eval_res_raw = eval_llm.invoke(prompt_val).content
        
        # Parse JSON
        try:
            import re
            json_str = re.search(r'\{.*\}', eval_res_raw, re.DOTALL).group(0)
            eval_dict = json.loads(json_str)
        except Exception as e:
            print("Failed to parse eval output:", eval_res_raw)
            eval_dict = {"accuracy_score": 0, "relevance_score": 0, "reasoning": "Failed to parse."}
            
        acc = eval_dict.get("accuracy_score", 0)
        rel = eval_dict.get("relevance_score", 0)
        
        total_acc += acc
        total_rel += rel
        
        results.append({
            "question": q,
            "expected": expected,
            "actual": actual,
            "accuracy": acc,
            "relevance": rel,
            "reasoning": eval_dict.get("reasoning", "")
        })

    avg_acc = total_acc / len(dataset)
    avg_rel = total_rel / len(dataset)
    
    print(f"\nEvaluation Complete! Avg Accuracy: {avg_acc}/10, Avg Relevance: {avg_rel}/10")
    
    # Save Report
    report_content = f"# Baseline RAG Evaluation Report\n\n"
    report_content += f"**Model:** gemini-2.0-flash\n"
    report_content += f"**Document:** Attention Is All You Need (PDF)\n"
    report_content += f"**Note:** Evaluated with an added 10-second delay between questions to stay well within Google AI Studio's 15 RPM free-tier limit.\n"
    report_content += f"**Average Accuracy:** {avg_acc}/10\n"
    report_content += f"**Average Relevance:** {avg_rel}/10\n\n"
    report_content += "## Detailed Results\n"
    
    for idx, r in enumerate(results):
        report_content += f"### Q{idx+1}: {r['question']}\n"
        report_content += f"- **Expected:** {r['expected']}\n"
        report_content += f"- **Actual:** {r['actual']}\n"
        report_content += f"- **Scores:** Accuracy = {r['accuracy']}/10, Relevance = {r['relevance']}/10\n"
        report_content += f"- **Reasoning:** {r['reasoning']}\n\n"
        
    report_path = r"C:\Users\Hardik\.gemini\antigravity-ide\brain\18461205-2004-4c24-a623-9bad7960ec70\evaluation_report.md"
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(report_content)
        
    print(f"Report saved to {report_path}")

if __name__ == "__main__":
    run_evaluation()
